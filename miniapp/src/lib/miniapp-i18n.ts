export type TranslationKey = keyof typeof messages.ru;

export function normalizeLanguage(value?: string | null): MiniAppLanguage {
  return value?.toLowerCase().startsWith("uz") ? "uz" : "ru";
}

export function t(
  language: MiniAppLanguage,
  key: TranslationKey,
  vars?: Record<string, string | number | null | undefined>
) {
  const template = messages[language][key] ?? messages.ru[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(vars?.[name] ?? ""));
}

export function getLanguageLabel(language: MiniAppLanguage) {
  return language === "uz"
    ? t(language, "common.languageUz")
    : t(language, "common.languageRu");
}

